var program = [];
var pc = 0;

function read() {
    var x = program[pc];
    pc ++;
    return x;
}

function step() {
    // console.log(`step pc=${pc}`);
    var op = read();
    // console.log(`op=${op}`);

    if (op == 1) {
        var p1 = read();
        var p2 = read();
        var p3 = read();
        // console.log(`ADDR ${p1} + ${p2} -> ${p3}`);
        var v1 = program[p1];
        var v2 = program[p2];
        var o = v1 + v2;
        // console.log(`VAL ${v1} + ${v2} -> ${o}`);
        program[p3] = o;
        // console.log('new program', program);
    }
    if (op == 2) {
        var p1 = read();
        var p2 = read();
        var p3 = read();
        // console.log(`ADDR ${p1} * ${p2} -> ${p3}`);
        var v1 = program[p1];
        var v2 = program[p2];
        var o = v1 * v2;
        // console.log(`VAL ${v1} * ${v2} -> ${o}`);
        program[p3] = o;
        // console.log('new program', program);
    }
    return (op != 99);
}

function run(opcodes) {
    if (typeof(opcodes) === 'string') {
        opcodes = opcodes.split(',').map(t => ~~t);
    }

    program = opcodes;
    pc = 0;

    var alive = true;
    while (alive) {
        // console.log('program', program);
        alive = step();
    }

    // console.log('FINAL program', program);
    // console.log('RESULT', program[0]);
    // console.log();
    // console.log();

    return program[0];
}

